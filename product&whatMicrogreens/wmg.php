<div class="para">
<p>
Microgreens are young vegetable greens that are approximately 1–3 inches (2.5–7.5 cm) tall. </br></br>

They have an aromatic flavor and concentrated nutrient content and come in a variety of colors and textures (1).</br></br>

Microgreens are considered baby plants, falling somewhere between a sprout and baby green.</br></br>

That said, they shouldn’t be confused with sprouts, which do not have leaves. Sprouts also have
 a much shorter growing cycle of 2–7 days, whereas microgreens are usually harvested 7–21 days 
 after germination, once the plant’s first true leaves have emerged.</br></br>

Microgreens are more similar to baby greens in that only their stems and leaves are considered edible. 
However, unlike baby greens, they are much smaller in size and can be sold before being harvested.</br></br>

This means that the plants can be bought whole and cut at home, keeping them alive until they are consumed.</br></br>

Microgreens are very convenient to grow, as they can be grown in a variety of locations, including outdoors,
 in greenhouses and even on your windowsill.</br></br>
</p>
